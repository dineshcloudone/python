
###### Indexing
a = "this is python"

print(a[2])

##### Negative Indexing

print(a[-1])

##### Positive Slicing

"""
syntax : string[start:end:step]
default values : 

    start : 0
    end : length of string
    step : 1
"""

print(a[1::2])


##### Negative Slicing

print(a[-1:-4:-2])


##### String reverse

print(a[::-1])