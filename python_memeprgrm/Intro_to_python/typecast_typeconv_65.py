'''
Type Casting : Assigning a data type to a value and it is two types
1. Implicit type casting [or] Type Conversion : Automatically data type is assigned to a value (widening conversion is followed )
2. Explicit type casting [or] Type conversion : Converting one data type to another data type by our own

    Syntax : datatype(object)
    
        datatype : the datatype function for which data type we want to convert
        object : for which object we want to convert datatype
        
        ex : int(object) to convert datatype into int
             float(object) to convert datatype into float
             complex(object) to convert datatype into complex
'''

a='1'
print(int(a))

b=2.1
print(int(b))

c='3.3'
print(float(c))